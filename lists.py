names = ["Harry", "Jonh", "Mike"]
#add something to the end of list
names.append("Dara")
print(f"The list of names is {names}")
#sort list
names.sort()
print(f"The list of names after sorting: {names}")
#reverse list
names.reverse()
print(f"The list of names after reversing: {names}")
#remove item from the end of list
names.pop()
print(f"The list of names after pop: {names}")